const metricOrder: MetricKey[] = [
  'Budget',
  'PaymentsOnChain',
  'ProtocolNetOutflow',
  'Actuals',
  'Forecast',
]

const toIsoMonthStart = (period: string, endOffsetMonths = 0) => {
  const [year, month] = period.split('/').map(Number)
  const date = new Date(Date.UTC(year, month - 1 + endOffsetMonths, 1, 0, 0, 0, 0))
  return date.toISOString()
}

const createRowsForMonth = (monthIndex: number): MetricRow[] => {
  const rows: MetricRow[] = []

  Object.entries(mockSeriesByPath).forEach(([path, metricValues]) => {
    metricOrder.forEach((metric) => {
      const value = metricValues[metric][monthIndex] ?? 0
      rows.push({
        dimensions: [{ name: 'budget', path }],
        metric,
        unit: 'DAI',
        value,
        sum: value,
      })
    })
  })

  return rows
}

export const seriesGranularityMonthlyFirtsLevel: SeriesItem[] = MONTHS.map(
  (period, monthIndex) => ({
    period,
    start: toIsoMonthStart(period),
    end: toIsoMonthStart(period, 1),
    rows: createRowsForMonth(monthIndex),
  }),
)
